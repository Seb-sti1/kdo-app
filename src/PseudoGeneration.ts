function newPseudo(): string {
    if (Math.floor(Math.random() * 2) == 0) {
        return `${m_animals[Math.floor(Math.random() * m_animals.length)]} ${m_adjectives[Math.floor(Math.random() * m_adjectives.length)]}`
    } else {
        return `${f_animals[Math.floor(Math.random() * f_animals.length)]} ${f_adjectives[Math.floor(Math.random() * f_adjectives.length)]}`
    }
}


export function generateUnique(existingNames: string[]): string {
    let n: string | null = null;
    while (n === null) {
        n = newPseudo()
        if (existingNames.includes(n))
            n = null;
    }
    return n;
}

export function generateUrl(name: string, apiKey: string, gist: string): string {
    const url = new URL(window.location.origin + "/kdo-app/");
    const searchParams = new URLSearchParams({k: apiKey, g: gist, n: name});
    url.search = searchParams.toString();
    return url.toString();
}

